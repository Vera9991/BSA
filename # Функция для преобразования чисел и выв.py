# Функция для преобразования чисел и вывода результатов
def transform(decimal_numbers):
    # Запрашиваем у пользователя выбор системы счисления
    system = input("В какую систему счисления перевести (2 или 16)? ")
    
    # Если выбрана двоичная система
    if system == "2":
        for num in decimal_numbers:
            # Преобразуем число в двоичную систему и выводим
            print(f"переведённое число в 2-ричную: {bin(num)}")
        # Вычисляем сумму всех чисел и преобразуем её в двоичную систему
        sum_bin = sum(decimal_numbers)
        print(f"Сумма в двоичной системе: {bin(sum_bin)}")
    
    # Если выбрана шестнадцатеричная система
    elif system == "16":
        for num in decimal_numbers:
            # Преобразуем число в шестнадцатеричную систему и выводим
            print(f"переведённое число в 16-ричную: {hex(num)}")
        # Вычисляем сумму всех чисел и преобразуем её в шестнадцатеричную систему
        sum_hex = sum(decimal_numbers)
        print(f"Сумма в шестнадцатеричной системе: {hex(sum_hex)}")
    
    # Если пользователь ввёл что-то неправильно
    else:
        print("Неверный выбор системы счисления.")

# Основная часть программы

# Список для хранения введённых цветов
colors = []

# Запрашиваем у пользователя три цвета
for i in range(3):
    color = input(f"Введите цвет {i+1} в формате HEX: ")
    
    # Проверяем, что цвет начинается с # и имеет длину 7 символов
    if color[0] != '#' or len(color) != 7:
        print("Неверный формат цвета. Пожалуйста, введите цвет в формате HEX, например, #b08fba.")
        continue  # Пропускаем этот ввод и запрашиваем снова
    
    # Добавляем цвет в список
    colors.append(color)

# Список для хранения десятичных чисел
decimal_numbers = []

# Преобразуем каждый цвет из HEX в десятичную систему
for color in colors:
    # Убираем символ # и преобразуем оставшуюся часть в десятичное число
    decimal = int(color[1:], 16)
    decimal_numbers.append(decimal)
    # Выводим результат преобразования
    print(f"переведённое число в 10-ричную: {decimal}")

# Вызываем функцию transform для преобразования и вывода результатов
transform(decimal_numbers)


